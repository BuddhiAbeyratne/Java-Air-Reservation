    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        int d = JOptionPane.showConfirmDialog(null, "Press Yes to update");

        if (d == 0) {

            int row = jTable1.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(rootPane, "Error: no row selected");
                return;
            }
            Airport a1 = new Airport((String) jTable1.getValueAt(row, 0));
            if(!a1.getAirportID().equals(jTextField5.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Cannot update non exhisting airport");
                return;
            }
            String airname = jTextField2.getText();
            String aircity = jTextField3.getText();
            String airstate = jTextField4.getText();
            boolean airactive = jCheckBox1.isSelected();
            boolean validity = true;
            String aircountry = jTextField1.getText();
            if (airname.isEmpty()) {
                validity = false;
                v2.setVisible(true);
            }
            if (aircity.isEmpty()) {
                validity = false;
                v3.setVisible(true);
            }
            if (airstate.isEmpty()) {
                validity = false;
                v3.setVisible(true);
            }
            if (aircountry.isEmpty()) {
                validity = false;
                v4.setVisible(true);
            }

            if (validity == true) {
                a1.setName(airname);
                a1.setCity(aircity);
                a1.setApstate(airstate);
                a1.setCountry(aircountry);
                a1.setActive(airactive);

                if (a1.save()) {
                    JOptionPane.showMessageDialog(rootPane, "Data saved successfully");
                    tableload();
                } else {
                    return;
                }
            }
            a1.setName(airname);
            a1.setCity(aircity);
            a1.setApstate(airstate);
            a1.setCountry(aircountry);
            a1.setActive(airactive);
            if (!a1.save()) {
                JOptionPane.showMessageDialog(rootPane, "Error: Data not saved");
            }
            this.tableload();

        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            return;
        }
        Airport a1 = new Airport(jTable1.getValueAt(row, 0).toString());
        jTextField5.setText(a1.getAirportID());
        jTextField2.setText(a1.getName());
        jTextField3.setText(a1.getCity());
        jTextField4.setText(a1.getApstate());
        jCheckBox1.setSelected(a1.isActive());
        jTextField1.setText(a1.getCountry());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String airid = jTextField5.getText();
        String airname = jTextField2.getText();
        String aircity = jTextField3.getText();
        String airstate = jTextField4.getText();
        String aircountry = jTextField1.getText();
        boolean airactive = jCheckBox1.isSelected();
        boolean validity = true;
        String pattern = "[A-Z]*";

        if (!airid.matches(pattern) || airid.isEmpty()) {
            validity = false;
            v1.setVisible(true);
        }
        if (airname.isEmpty()) {
            validity = false;
            v2.setVisible(true);
        }
        if (aircity.isEmpty()) {
            validity = false;
            v3.setVisible(true);
        }
        if (airstate.isEmpty()) {
            validity = false;
            v3.setVisible(true);
        }
        if (aircountry.isEmpty()) {
            validity = false;
            v4.setVisible(true);
        }

        if (validity == true) {
            Airport a1 = new Airport();
            a1.setAirportID(airid);
            a1.setName(airname);
            a1.setCity(aircity);
            a1.setApstate(airstate);
            a1.setCountry(aircountry);
            a1.setActive(airactive);

            if (a1.save()) {
                JOptionPane.showMessageDialog(rootPane, "Data saved successfully");
                tableload();
            } else {
                return;
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

